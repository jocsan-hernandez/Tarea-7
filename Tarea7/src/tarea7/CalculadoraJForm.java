    private void IgualActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_IgualActionPerformed
        this.numero2= Float.parseFloat(this.jTextField1.getText());
        if(this.metodo.equals("suma")){
            this.resultado= numero1+numero2;
            if(resultado%1==0){
            this.jTextField1.setText(String.valueOf(Math.round(resultado)));
            }else{
            this.jTextField1.setText(String.valueOf(resultado));}}
        if(this.metodo.equals("resta")){
             this.resultado= numero1-numero2;
            if(resultado%1==0){
            this.jTextField1.setText(String.valueOf(Math.round(resultado)));
            }else{
            this.jTextField1.setText(String.valueOf(resultado));}    
        }
        
        if(this.metodo.equals("multiplicacion")){
             this.resultado= numero1*numero2;
            if(resultado%1==0){
            this.jTextField1.setText(String.valueOf(Math.round(resultado)));
            }else{
            this.jTextField1.setText(String.valueOf(resultado));}    
        }
        
        if(this.metodo.equals("division")){
            if(numero2==0){
                this.jTextField1.setText("NAN");
            }else{
            this.resultado= numero1/numero2;
            if(resultado%1==0){
            this.jTextField1.setText(String.valueOf(Math.round(resultado)));
            }else{
            this.jTextField1.setText(String.valueOf(resultado));}}    
        }
        this.banderaoperacion=true;
        this.banderapunto=false;
        this.metodo="";
    }//GEN-LAST:event_IgualActionPerformed

